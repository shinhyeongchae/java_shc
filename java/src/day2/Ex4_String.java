package day2;

public class Ex4_String {

	public static void main(String[] args) {
		String str = "Hello";
		str = str + " world";
		System.out.println(str);
		str = str +	'!';
		System.out.println(str);
		str = str + 123;
		System.out.println(str);
		str = str + 3.45;
		System.out.println(str);
		str = str + true;
		System.out.println(str);
	}

}
