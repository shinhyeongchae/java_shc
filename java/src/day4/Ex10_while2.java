package day4;

public class Ex10_while2 {

	public static void main(String[] args) {
		/*1부터 5까지의 합을 구하는 코드를 while문을 이용하여 작성하시오
		 * 
		 * */
	
		int i =1, sum =0;
		while(i<=5) {
			sum = sum + i; 
			i++;
		}
		System.out.println("1부터 5까지의 합 : " + sum );
		i = 1;
		sum = 0;
	
			
			
			
			
		
	
	
	
	
	
	
	
	}
	

}
